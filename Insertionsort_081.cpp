#include <iostream>
using namespace std;
int main() {
    int n;
    cout << "Enter array size: ";
    cin >> n;
    int arr[n];
    cout << "Enter array elements:";
    for (int i = 0; i < n; i++)cin >> arr[i];
    int c = 0;
    for (int i = 1; i < n; i++) {
        int key = arr[i];
        int j = i - 1;
        while (j >= 0 && arr[j] > key) {
            c++;
            arr[j + 1] = arr[j];
            j--;
        }
        if (j >= 0)
            c++;
        arr[j + 1] = key;
    }
    cout << "Sorted array:\n";
    for (int i = 0; i < n; i++)
        cout << arr[i] << " ";
    cout << "\nTotal comparisons: " << c;
    return 0;
}

